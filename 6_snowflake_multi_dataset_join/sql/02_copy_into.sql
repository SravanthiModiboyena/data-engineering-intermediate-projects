
USE DATABASE ECOMMERCE_DB;
USE SCHEMA RAW;

-- Load Customers
COPY INTO RAW.customers
FROM @ANALYTICS.MY_STAGE/customers.csv
FILE_FORMAT = (
    TYPE = CSV
    FIELD_OPTIONALLY_ENCLOSED_BY = '"'
    SKIP_HEADER = 1
)
ON_ERROR = 'CONTINUE';

-- Load Products
COPY INTO RAW.products
FROM @ANALYTICS.MY_STAGE/products.csv
FILE_FORMAT = (
    TYPE = CSV
    FIELD_OPTIONALLY_ENCLOSED_BY = '"'
    SKIP_HEADER = 1
)
ON_ERROR = 'CONTINUE';

-- Load Orders
COPY INTO RAW.orders
FROM @ANALYTICS.MY_STAGE/orders.csv
FILE_FORMAT = (
    TYPE = CSV
    FIELD_OPTIONALLY_ENCLOSED_BY = '"'
    SKIP_HEADER = 1
)
ON_ERROR = 'CONTINUE';

